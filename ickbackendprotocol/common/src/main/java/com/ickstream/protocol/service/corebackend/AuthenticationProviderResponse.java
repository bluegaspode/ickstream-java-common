package com.ickstream.protocol.service.corebackend;

public class AuthenticationProviderResponse {
    private String id;
    private String name;
    private String icon;
    private String url;
    private String addIdentityUrl;
    private String redirectUrl;
    private String apiKey;
    private String apiSecret;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getAddIdentityUrl() {
        return addIdentityUrl;
    }

    public void setAddIdentityUrl(String addIdentityUrl) {
        this.addIdentityUrl = addIdentityUrl;
    }

    public String getRedirectUrl() {
        return redirectUrl;
    }

    public void setRedirectUrl(String redirectUrl) {
        this.redirectUrl = redirectUrl;
    }

    public String getApiKey() {
        return apiKey;
    }

    public void setApiKey(String apiKey) {
        this.apiKey = apiKey;
    }

    public String getApiSecret() {
        return apiSecret;
    }

    public void setApiSecret(String apiSecret) {
        this.apiSecret = apiSecret;
    }
}
